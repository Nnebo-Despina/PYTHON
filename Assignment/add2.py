"""
This is a code to print out the;
Sum of two numbers
Difference of two numbers
Product of two numbers
Quotient of two numbers
"""

print("What is your first number?")
num1 = int(input("Number 1: "))

print("What is your second number?")
num2 = int(input("Number 2: "))

sum = num1 + num2 
print(f"This is the sum of the two numbers; {sum}") #addition

sum = num1 - num2
print(f"This is the difference of the two numbers; {sum}") #subtraction

sum = num1 * num2
print(f"This is the product of the two numbers; {sum}") #multiplication

sum = num1 / num2
print(f"This is the quotient of the two numbers; {sum}") #division